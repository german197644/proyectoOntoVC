    @Override
    public void actionPerformed(ActionEvent e) {
        Object evt = e.getSource();
        if (evt.equals(btn_anterior)) {
            if (panelSword.isVisible()) {
                panelSword.setVisible(false);
                panelOntologico.setVisible(true);
                contenedor.add(panelOntologico);
                deshabilitarBotones();
                contenedor.validate();
            } else if (panelOntologico.isVisible()) {
                panelOntologico.setVisible(false);
                panelConexion.setVisible(true);
                contenedor.add(panelConexion);
                deshabilitarBotones();
                contenedor.validate();
            }
        } else if (evt.equals(btn_siguiente)) {
            if (panelConexion.isVisible()) {
                panelConexion.setVisible(false);
                panelOntologico.setVisible(true);
                contenedor.add(panelOntologico);
                deshabilitarBotones();
                contenedor.validate();
            } else if (panelOntologico.isVisible()) {
                panelOntologico.setVisible(false);
                panelSword.setVisible(true);
                contenedor.add(panelSword);
                deshabilitarBotones();
                contenedor.validate();
            }
        }
        pack();
        setLocationRelativeTo(null);
    }
